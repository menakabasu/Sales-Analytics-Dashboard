--Find total customers by countries
select country as Country ,count( customer_key)  as Total_customers from gold.dim_customers
GROUP BY country
ORDER BY Total_customers DESC
--calclaute total profit
select 
	sum(f.sales_amount-(p.cost*f.quantity)) as Total_Profit
	from gold.fact_sales f
	join gold.dim_products p
	on f.product_key=p.product_key
--Profit for each Product
SELECT
    p.product_name,
    SUM(s.sales_amount - (p.cost * s.quantity)) AS profit
FROM gold.fact_sales s
JOIN gold.dim_products p
ON s.product_key = p.product_key
GROUP BY p.product_name
ORDER BY profit DESC;
--Profit for each Category
SELECT
    p.category,
    SUM(s.sales_amount - (p.cost * s.quantity)) AS total_profit
FROM gold.fact_sales s
JOIN gold.dim_products p
ON s.product_key = p.product_key
GROUP BY p.category
ORDER BY total_profit DESC;

