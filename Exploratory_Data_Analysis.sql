--Which age group generated the most revenue	
select
sum(sales_amount) as Revenue,
 case 
	when year(getdate())-year(birthdate) <20 then 'young'
	when year(getdate())-year(birthdate) between 20 and 40 then 'old'
	else 'too_old ' 
	end as age
from gold.dim_customers c
left join gold.fact_sales f
on c.customer_key=f.customer_key
group by 
case 
	when year(getdate())-year(birthdate) <20 then 'young'
	when year(getdate())-year(birthdate) between 20 and 40 then 'old'
	else 'too_old ' 
	end 
order by Revenue desc

--%contribution of each product category to the total revenue

select 
	p.category as Category ,
	round((SUM(f.sales_amount) * 100.0 /
        (SELECT SUM(sales_amount) FROM gold.fact_sales)),2) AS revenue_contribution
from gold.dim_products p
left join gold.fact_sales f
on f.product_key=p.product_key
group by category
order by revenue_contribution desc


